	GPIOE.CR2=0x00;
	GPIOE.ODR.byte=0x00;

	GPIOF.DDR=0x00; //
	GPIOF.CR1=0x00;
	GPIOF.CR2=0x00;
	GPIOF.ODR.byte=0x00;
	
	GPIOG.DDR=0x00; //
